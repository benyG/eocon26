  `lang_fr` BOOLEAN NOT NULL DEFAULT false,
  `lang_en` BOOLEAN NOT NULL DEFAULT false,
  `lang_other` VARCHAR(191) NULL,
  `topic_main` VARCHAR(191) NULL,
  `topics_secondary` JSON NOT NULL DEFAULT ('[]'),
  `keywords` TEXT NULL,
  `tech_level` VARCHAR(191) NULL,
  `certifications` TEXT NULL,
  `linkedin` VARCHAR(191) NULL,
  `twitter` VARCHAR(191) NULL,
  `github` VARCHAR(191) NULL,
  `scholar` VARCHAR(191) NULL,
  `website` VARCHAR(191) NULL,
  `email` VARCHAR(191) NULL,
  `conferences_past` TEXT NULL,
  `videos_urls` TEXT NULL,
  `publications` TEXT NULL,
  `p1` INT NOT NULL DEFAULT 0,
