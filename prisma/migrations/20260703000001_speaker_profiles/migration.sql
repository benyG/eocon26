  `type` VARCHAR(191) NOT NULL DEFAULT 'other',
  `title` VARCHAR(191) NULL,
  `url` TEXT NULL,
  `reliability` VARCHAR(191) NOT NULL DEFAULT 'secondary',
  `consulted_at` DATETIME(3) NULL,
  `notes` TEXT NULL,
  `created_at` DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3),
  PRIMARY KEY (`id`),
  CONSTRAINT `speaker_sources_speaker_profile_id_fkey` FOREIGN KEY (`speaker_profile_id`) REFERENCES `speaker_profiles` (`id`) ON DELETE CASCADE ON UPDATE CASCADE
