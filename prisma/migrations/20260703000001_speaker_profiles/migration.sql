CREATE TABLE `speaker_contacts` (
  `id` INT NOT NULL AUTO_INCREMENT,
  `speaker_profile_id` INT NOT NULL,
  `event_label` VARCHAR(191) NOT NULL DEFAULT 'EOCON 2026',
  `contact_status` VARCHAR(191) NOT NULL DEFAULT 'to_research',
  `format_proposed` VARCHAR(191) NULL,
  `topic_proposed` TEXT NULL,
  `assignee_id` INT NULL,
  `contacted_at` DATETIME(3) NULL,
  `response_at` DATETIME(3) NULL,
  `next_followup_at` DATETIME(3) NULL,
  `notes` TEXT NULL,
  `created_at` DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3),
  `updated_at` DATETIME(3) NOT NULL,
  PRIMARY KEY (`id`),
  CONSTRAINT `speaker_contacts_speaker_profile_id_fkey` FOREIGN KEY (`speaker_profile_id`) REFERENCES `speaker_profiles` (`id`) ON DELETE CASCADE ON UPDATE CASCADE
) DEFAULT CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci;

